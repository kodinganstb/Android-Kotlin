// MEMBAHAS TENTANG "Variable" Pada bahasa pemrograman Kotlin
// Sebelum terjun ke kode di bawah ini, alangkah baiknya teman-teman tahu apa itu Variable?
// Variable merupakan penampung untuk satu bagian data (wadah), contohnya seperti variable NAMA berisi tentang data string 'Fajar Hardeka'
// Dalam Kotlin memiliki 2 cara untuk mendeklarasikan variable yaitu Val and Var
//
